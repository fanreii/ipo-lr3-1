number = int(input("Введите целое число: ")) # запрос ввода числа с клавиатуры 
if number %2 == 0: # использование оператора ветвления if-else с условием кратности числа двум
    print("Введенное число является четным") # вывод результата с верным условием
else: # else есть else
    print("Введенное число является нечетным") # вывод результата, если условие ложно
    